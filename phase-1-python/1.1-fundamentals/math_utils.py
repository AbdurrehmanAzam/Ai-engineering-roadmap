def check_even_odd(number):
    """
    Checks whether a number is even or odd.

    Args:
        number (int): Any valid integer.

    Returns:
        str: "Even" or "Odd"

    Created by: Muhammad Abdurrehman Azam
    Last edited: May 11, 2026
    """
    if number % 2 == 0:
        return "Even"
    else:
        return "Odd"

